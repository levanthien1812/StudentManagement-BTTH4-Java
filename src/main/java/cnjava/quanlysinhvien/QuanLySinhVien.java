    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setCurrentDirectory(new File(System.getProperty("user.home")));
        int result = fileChooser.showSaveDialog(this);
        
        try {
            if (result == JFileChooser.APPROVE_OPTION) {
                File fileToSave = fileChooser.getSelectedFile();
                int option;
                if (fileToSave.exists()) {
                    option = JOptionPane.showConfirmDialog(this, "File này đã tồn tại! Bạn có muốn ghi đè nó không?", "Question", 2);
                    
                    if (option == JOptionPane.CANCEL_OPTION) {
                        return;
                    }
                }
                
                String filePath = fileToSave.getAbsolutePath();
    //            BufferedWriter outputWriter = new BufferedWriter(new FileWriter(filePath));
                FileOutputStream fos = new FileOutputStream(filePath);
                ObjectOutputStream oos = new ObjectOutputStream(fos);
                for (int i = 0; i < this.dsSinhVien.toArray().length; i++) {
                    oos.writeObject(this.dsSinhVien.get(i));
                    System.out.println(this.dsSinhVien.get(i));
                }
                
                JOptionPane.showMessageDialog(this, "File txt đã được lưu!");
                oos.flush();
                oos.close();
            }
        } catch (IOException e) {
            System.out.print(e);
        }
        
    }//GEN-LAST:event_jButton9ActionPerformed

    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setCurrentDirectory(new File(System.getProperty("user.home")));
        int result = fileChooser.showOpenDialog(this);
        
        try {
            if (result == JFileChooser.APPROVE_OPTION) {
                File fileToRead = fileChooser.getSelectedFile();
                String filePath = fileToRead.getAbsolutePath();
                if (fileToRead.getName().endsWith(".txt") == false) {
                    JOptionPane.showMessageDialog(this, "File đượcc chọn không phải file txt!");
                    return;
                };
                JOptionPane.showMessageDialog(this, "Đọc file txt thành công!");
                FileInputStream fis = new FileInputStream(filePath);
                ObjectInputStream ois = new ObjectInputStream(fis);
//                outputWriter = new BufferedWriter(new FileWriter(filePath));
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                boolean conti = true;
                while (conti == true) {
                    SinhVien sinhVien = (SinhVien) ois.readObject();
                    if (sinhVien != null) {
                        this.dsSinhVien.add(sinhVien);
                        Object[] row = {sinhVien.MSSV, sinhVien.Ten, sinhVien.NgaySinh};
                        model.addRow(row);
                    } else {
                        conti = false;
                    }
                } 
                JOptionPane.showMessageDialog(this, "Mở file txt thành công");
                ois.close();  
            }
        } catch (Exception e) {
            System.out.print(e);
        }
    }//GEN-LAST:event_jButton8ActionPerformed

