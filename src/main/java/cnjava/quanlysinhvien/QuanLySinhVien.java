    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String mssv = jTextField1.getText();
        String ten = jTextField2.getText();
        String ngaySinh = jTextField3.getText();
        
        if (mssv.length() == 0 || ten.length() == 0 || ngaySinh.length() == 0) {
            JOptionPane.showMessageDialog(this, "Mã số sinh viên, tên và ngày sinh không được để trống!");
            return;
        }
        
        for (SinhVien sv: this.dsSinhVien) {
            if (sv.MSSV.equals(mssv)) {
                JOptionPane.showMessageDialog(this, "Mã số sinh viên " + mssv + " đã tồn tại!");
                return;
            }
        }
        
        String[] dateParts = ngaySinh.split("/");
        if (dateParts.length != 3) {
            JOptionPane.showMessageDialog(this, "Mã số sinh viên, tên và ngày sinh không được để trống!");
            return;
        }
        
        SinhVien sv = new SinhVien(mssv, ten, ngaySinh);
        this.dsSinhVien.add(sv);
        
        Object[] row = {sv.MSSV, sv.Ten, sv.NgaySinh};
        
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.addRow(row);
    }//GEN-LAST:event_jButton1ActionPerformed

